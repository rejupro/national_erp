                                <h3>Rising Damp</h3>
                                <p class="text-justify">This is when water from the ground rises through the brickwork, stonework or through the mortar. It is unsightly and unhealthy, usually a problem in houses built pre 1925 when damp proof courses were not commonly installed. It is less common in modern properties, but can occur where the original damp proof course has become damaged or the ground level around the property has been raised with the addition of paths, flower beds or driveways, which have bridged the original damp proof course.</p>
                                <h3>Penetrating Damp</h3>
                                <p class="text-justify">Is the result of water successfully penetrating horizontally from the outside of the building through the fabric of the wall. It can affect all buildings and is most commonly caused by problems with the roof, brickwork or windows – leaky pipes, blocked gutters, defective pointing or poorly maintained brickwork. It can be damaging even if it does not penetrate all the way through a wall, potentially leading to more serious problems such as dry rot, wet rot or structural damage.</p>
                                <h3>Condensation</h3>
                                <p class="text-justify">This is the most common form of dampness within a building, and can cause unsightly black spot mould patches, peeling paint work and a musty smell. Condensation happens in almost every property as a result of the moisture created by everyday living, from breathing to boiling the kettel, moisture is present in the warm atmosphere of your home and when the structure cools down the moisture condenses and settles on the cold internal surfaces.<br>Newton System 800 is a range of BBA approved damp proof membranes and ancillaries which are quick and clean to install and offer a permanent solution for treating damp walls.</p>
